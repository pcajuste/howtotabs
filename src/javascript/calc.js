(function(){
    "use strict";

    exports.add = function add(a, b){
       return a + b;
    };

    exports.substract = function substract(a, b){
       return a - b;
    };
   
    exports.multiply = function multiply(a, b){
       return a * b;
    };
   
    exports.divide = function divide(a, b){
      return a / b;
    };
    
})();